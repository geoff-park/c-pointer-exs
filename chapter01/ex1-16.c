/* 1-16 int형 주소를 char형 주소로 변환하여 각각의 메모리를 1바이트씩 접근 */
#include <stdio.h>
void main() {
  int n = 0x44434241;  // 십진수 1145258561
  // 16진수 2개는 1바이트
  // 메모리 저장 방식: 리틀 엔디안(little-endian)
  // 메모리에 데이터를 저장할 때 낮은 주소에서 높은 주소로 하위 바이트부터 상위 바이트까지 저장

  // GPT's 설명
  // 가장 하위 바이트(LSB)가 가장 낮은 주소에 먼저 저장된다.
  /*
  주소 ↑ (큰 주소)
  [ base+3 ] : 0x44  ('D')   ← 가장 상위 바이트(MSB)
  [ base+2 ] : 0x43  ('C')
  [ base+1 ] : 0x42  ('B')
  [ base+0 ] : 0x41  ('A')   ← 가장 하위 바이트(LSB), 가장 낮은 주소
  주소 ↓ (작은 주소)
  */

  printf("%d %c\n", *(char*)&n, *(char*)&n);              // 65 A
  printf("%d %c\n", *((char*)&n + 1), *((char*)&n + 1));  // 66 B
  printf("%d %c\n", *((char*)&n + 2), *((char*)&n + 2));  // 67 C
  printf("%d %c\n", *((char*)&n + 3), *((char*)&n + 3));  // 68 D
}